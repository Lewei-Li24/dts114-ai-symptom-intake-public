def save_profile(payload: dict[str, Any], profile_id: int | None = None) -> int:
    db = get_db()
    values = (
        payload.get("display_name", "") or "New profile",
        nullable_int(payload.get("age")),
        payload.get("sex", ""),
        nullable_float(payload.get("height_cm")),
        nullable_float(payload.get("weight_kg")),
        payload.get("allergies", ""),
        payload.get("current_medications", ""),
        payload.get("chronic_conditions", ""),
        payload.get("past_history", ""),
        payload.get("family_history", ""),
        payload.get("emergency_contact", ""),
        1 if str(payload.get("consent_use_history", "")).lower() in {"1", "true", "on", "yes"} else 0,
    )
    if profile_id and get_profile(profile_id) is not None:
        db.execute(
            """
            UPDATE user_profiles
            SET display_name = ?, age = ?, sex = ?, height_cm = ?, weight_kg = ?,
                allergies = ?, current_medications = ?, chronic_conditions = ?,
                past_history = ?, family_history = ?, emergency_contact = ?,
                consent_use_history = ?, updated_at = CURRENT_TIMESTAMP
            WHERE id = ?
            """,
            (*values, profile_id),
        )
        saved_id = profile_id
    else:
        cursor = db.execute(
            """
            INSERT INTO user_profiles
                (display_name, age, sex, height_cm, weight_kg, allergies, current_medications,
                 chronic_conditions, past_history, family_history, emergency_contact, consent_use_history)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            values,
        )
        saved_id = int(cursor.lastrowid)
    db.commit()
    return saved_id


