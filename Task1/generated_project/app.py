        "Ask one concise follow-up question at a time until enough context is collected for non-diagnostic care guidance. "
        "Return strict JSON with keys: assistant_message, complete, extracted, missing_fields. "
        "extracted must contain primary_symptom, duration, severity, additional_context, associated_symptoms, "
