def fallback_intake_chat_reply(history: list[dict[str, str]]) -> dict[str, Any]:
    user_turns = [item["content"] for item in history if item["role"] == "user"]
    combined = " ".join(user_turns)
    symptom_hint = extract_primary_symptom(history) or "this symptom"
    duration_known = bool(re.search(r"\b(hour|hours|day|days|week|weeks|month|months|today|yesterday|morning|night)\b|小时|天|周|月|今天|昨天|早上|晚上", combined, re.IGNORECASE))
    severity_known = parse_severity_from_text(combined) > 0
    medicine_known = bool(re.search(r"\b(medicine|medication|drug|allergy|allergic|ibuprofen|paracetamol|acetaminophen|aspirin)\b|药|过敏|布洛芬|对乙酰氨基酚", combined, re.IGNORECASE))
    associated_known = bool(re.search(r"\b(fever|nausea|dizzy|dizziness|rash|vomit|breath|pain)\b|发烧|恶心|头晕|皮疹|呕吐|呼吸|疼", combined, re.IGNORECASE))
    prompts = []
    if not duration_known:
        prompts.append(f"How long has {symptom_hint} been happening, and did it start suddenly or gradually?")
    if not severity_known:
        prompts.append(f"On a scale of 0 to 10, how severe is {symptom_hint} right now?")
    if not associated_known:
        prompts.append("Are there any associated symptoms, such as fever, dizziness, nausea, rash, pain elsewhere, vomiting, or trouble breathing?")
    if not medicine_known:
        prompts.append("Have you taken any medicines, and do you have allergies, pregnancy, chronic conditions, recent injury, or unusual exposure?")
    if not prompts:
        prompts.append("What has changed since the symptom started, and what makes it better or worse?")
    if len(user_turns) <= len(prompts):
        return {"assistant_message": prompts[len(user_turns) - 1], "complete": False, "extracted": {}, "source": "fallback"}

    extracted = normalize_extracted_symptom({}, history)
    return {
        "assistant_message": (
            "Thanks. I have enough information to create a structured summary and safe care guidance. "
            "Please review everything before acting."
        ),
        "complete": True,
        "extracted": extracted,
        "source": "fallback",
    }


def normalize_extracted_symptom(extracted: dict[str, Any], history: list[dict[str, str]]) -> dict[str, Any]:
    user_text = " ".join(item["content"] for item in history if item["role"] == "user")
    context_parts = [
        ("full_chat_notes", extracted.get("additional_context") or user_text),
        ("associated_symptoms", extracted.get("associated_symptoms")),
        ("medicines_taken", extracted.get("medicines_taken")),
        ("allergies", extracted.get("allergies")),
        ("relevant_history", extracted.get("relevant_history")),
        ("user_concerns", extracted.get("user_concerns")),
    ]
    additional_context = "\n".join(
        f"{label}: {str(value).strip()}"
        for label, value in context_parts
        if str(value or "").strip()
    )
    return {
        "primary_symptom": str(extracted.get("primary_symptom") or extract_primary_symptom(history)).strip(),
        "duration": str(extracted.get("duration") or extract_duration(user_text)).strip(),
        "severity": parse_severity(extracted.get("severity") or parse_severity_from_text(user_text)),
        "additional_context": additional_context.strip() or user_text.strip(),
    }


def extract_primary_symptom(history: list[dict[str, str]]) -> str:
    for item in history:
        if item["role"] == "user":
            text = item["content"].strip()
            return text[:120] or "Unspecified symptom"
    return "Unspecified symptom"


def extract_duration(text: str) -> str:
    duration = re.search(
        r"\b(\d+\s*(?:minutes?|hours?|days?|weeks?|months?)|today|yesterday|this morning|tonight|刚刚|今天|昨天|[一二三四五六七八九十\d]+天)\b",
        text,
        re.IGNORECASE,
    )
    return duration.group(1) if duration else "Not clearly specified"


