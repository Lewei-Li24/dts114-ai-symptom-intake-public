def create_symptom_session(payload: dict[str, Any]) -> dict[str, Any]:
    severity = parse_severity(payload.get("severity"))
    combined_text = " ".join(
        [
            str(payload.get("primary_symptom", "")),
            str(payload.get("duration", "")),
            str(payload.get("additional_context", "")),
        ]
    )
    red_flags = detect_red_flags(combined_text, severity)
    safety_triggered = bool(red_flags)
    triage_level = "urgent" if safety_triggered else triage_level_from_severity(severity)
    profile_id = nullable_int(payload.get("profile_id"))
    user_label = payload.get("user_label") or "anonymous-user"

    db = get_db()
    cursor = db.execute(
        "INSERT INTO user_sessions (profile_id, user_label, triage_level, safety_triggered) VALUES (?, ?, ?, ?)",
        (profile_id, user_label, triage_level, int(safety_triggered)),
    )
    session_id = int(cursor.lastrowid)
    db.execute(
        """
        INSERT INTO symptom_inputs
            (session_id, primary_symptom, duration, severity, additional_context, red_flags)
        VALUES (?, ?, ?, ?, ?, ?)
        """,
        (
            session_id,
            payload["primary_symptom"],
            payload["duration"],
            severity,
            payload.get("additional_context", ""),
            json.dumps(red_flags),
        ),
    )
    if not safety_triggered:
        for question in generate_followup_questions(
            {
                "primary_symptom": payload["primary_symptom"],
                "duration": payload["duration"],
                "severity": severity,
                "additional_context": payload.get("additional_context", ""),
            }
        ):
            insert_followup_question(session_id, question)
    db.commit()
    return {
        "session_id": session_id,
        "triage_level": triage_level,
        "safety_triggered": safety_triggered,
        "red_flags": red_flags,
        "questions": [row["question_text"] for row in get_questions(session_id)] if not safety_triggered else [],
        "disclaimer": DISCLAIMER_TEXT,
        "next_url": url_for("safety_notice_page", session_id=session_id)
        if safety_triggered
        else url_for("followup_questions_page", session_id=session_id),
    }


def parse_severity(value: Any) -> int:
    try:
        return max(0, min(10, int(value)))
    except (TypeError, ValueError):
        return 0


def parse_severity_from_text(text: str) -> int:
    explicit = re.search(r"\b(10|[0-9])\s*/\s*10\b", text)
    if explicit:
        return parse_severity(explicit.group(1))
    number = re.search(r"\b(?:severity|pain|程度|疼痛)[^\d]*(10|[0-9])\b", text, re.IGNORECASE)
    if number:
        return parse_severity(number.group(1))
    if re.search(r"\b(severe|worst|unbearable|剧烈|严重|难以忍受)\b", text, re.IGNORECASE):
        return 8
    if re.search(r"\b(mild|slight|轻微)\b", text, re.IGNORECASE):
        return 2
    return 4


def detect_red_flags(text: str, severity: int) -> list[str]:
    normalized = text.lower()
    matched = []
    for rule_name, pattern in RED_FLAG_RULES:
        for match in re.finditer(pattern, normalized, re.IGNORECASE):
            if not is_negated_match(normalized, match.start()):
                matched.append(rule_name)
                break
    if severity >= 9:
        matched.append("very_high_severity")
    return sorted(set(matched))


def is_negated_match(text: str, match_start: int) -> bool:
    prefix = text[max(0, match_start - 28) : match_start]
    return bool(re.search(r"\b(no|not|without|deny|denies|denied|没有|无|未|不)\b\s*$", prefix, re.IGNORECASE))


def triage_level_from_severity(severity: int) -> str:
    if severity >= 7:
        return "soon"
    if severity >= 4:
        return "routine"
    return "self_monitor"


