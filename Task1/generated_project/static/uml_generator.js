const umlForm = document.getElementById("uml-form");
const umlStatus = document.getElementById("uml-status");
const umlOutput = document.getElementById("uml-output");
const umlExplanation = document.getElementById("uml-explanation");
const umlRenderPanel = document.getElementById("uml-render-panel");
const umlRenderedImage = document.getElementById("uml-rendered-image");
const umlRenderEmpty = document.getElementById("uml-render-empty");
const umlSvgLink = document.getElementById("uml-svg-link");
const umlPngLink = document.getElementById("uml-png-link");

function setRenderLinks(svgUrl, pngUrl) {
    if (!umlRenderedImage || !umlSvgLink || !umlPngLink || !umlRenderPanel || !umlRenderEmpty) {
        return;
    }
    if (!svgUrl || !pngUrl) {
        umlRenderPanel.classList.add("is-empty");
        umlRenderedImage.removeAttribute("src");
        umlRenderEmpty.textContent = "The rendered diagram will appear automatically after generation.";
        umlSvgLink.href = "#";
        umlPngLink.href = "#";
        umlSvgLink.classList.add("disabled-link");
        umlPngLink.classList.add("disabled-link");
        return;
    }
    umlRenderPanel.classList.remove("is-empty");
    umlRenderedImage.src = svgUrl;
    umlRenderEmpty.textContent = "";
    umlSvgLink.href = svgUrl;
    umlPngLink.href = pngUrl;
    umlSvgLink.classList.remove("disabled-link");
    umlPngLink.classList.remove("disabled-link");
}

if (umlForm) {
    umlForm.addEventListener("submit", async (event) => {
        event.preventDefault();
        const businessProblem = document.getElementById("business-problem").value.trim();
        const diagramType = document.getElementById("diagram-type").value;
        if (!businessProblem) {
            umlStatus.textContent = "Enter a business problem first.";
            return;
        }
        umlStatus.textContent = "Calling APIFree through Flask...";
        umlOutput.textContent = "Generating UML...";
        umlExplanation.textContent = "";
        setRenderLinks(null, null);
        try {
            const response = await fetch("/api/uml/generate", {
                method: "POST",
                headers: {"Content-Type": "application/json"},
                body: JSON.stringify({business_problem: businessProblem, diagram_type: diagramType}),
            });
            const data = await response.json();
            if (!response.ok) {
                umlStatus.textContent = data.message || data.error || "UML generation failed.";
                umlOutput.textContent = JSON.stringify(data, null, 2);
                setRenderLinks(null, null);
                return;
            }
            umlStatus.textContent = `${data.title} generated by ${data.provider || "AI model"}.`;
            umlOutput.textContent = data.plantuml;
            umlExplanation.textContent = data.explanation || "";
            setRenderLinks(data.render_svg_url, data.render_png_url);
        } catch (error) {
            umlStatus.textContent = "Network or server error while generating UML.";
            umlOutput.textContent = String(error);
            setRenderLinks(null, null);
        }
    });
}
