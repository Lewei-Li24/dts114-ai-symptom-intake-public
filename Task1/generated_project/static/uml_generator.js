const umlForm = document.getElementById("uml-form");
const umlStatus = document.getElementById("uml-status");
const umlOutput = document.getElementById("uml-output");
const umlExplanation = document.getElementById("uml-explanation");

if (umlForm) {
    umlForm.addEventListener("submit", async (event) => {
        event.preventDefault();
        const businessProblem = document.getElementById("business-problem").value.trim();
        const diagramType = document.getElementById("diagram-type").value;
        if (!businessProblem) {
            umlStatus.textContent = "Enter a business problem first.";
            return;
        }
        umlStatus.textContent = "Calling APIFree through Flask...";
        umlOutput.textContent = "Generating UML...";
        umlExplanation.textContent = "";
        try {
            const response = await fetch("/api/uml/generate", {
                method: "POST",
                headers: {"Content-Type": "application/json"},
                body: JSON.stringify({business_problem: businessProblem, diagram_type: diagramType}),
            });
            const data = await response.json();
            if (!response.ok) {
                umlStatus.textContent = data.message || data.error || "UML generation failed.";
                umlOutput.textContent = JSON.stringify(data, null, 2);
                return;
            }
            umlStatus.textContent = `${data.title} generated by ${data.provider || "AI model"}.`;
            umlOutput.textContent = data.plantuml;
            umlExplanation.textContent = data.explanation || "";
        } catch (error) {
            umlStatus.textContent = "Network or server error while generating UML.";
            umlOutput.textContent = String(error);
        }
    });
}
